package kr.manamana.clas;

public class Student {
	
	 String name;
	 String grade;
	 
	 public Student() {
		 
	 }

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}
	 

}
